from aiogram import Router
from aiogram.filters import Command
from aiogram.types import Message

router = Router(name="help")


@router.message(Command("help"))
async def handle_help(message: Message) -> None:
    await message.answer(
        "Что я умею:\n\n"
        "/where — «куда ехать?»: отправьте геолокацию (или выберите домашний район), "
        "подскажу, где сейчас лучше работать. Можно и без команды — просто пришлите "
        "местоположение.\n"
        "/kef — текущий кэф по районам, с честной пометкой источника "
        "(реальный радар / по ценам / синтетика).\n"
        "/finance — итоги дня: доход, расходы, чистыми, ₽/час.\n"
        "/plan — лучшие часы для работы сегодня.\n"
        "/profile — заполнить профиль по шагам: машина, топливо, аренда, "
        "домашний район, график.\n"
        "/link — связать этот чат с аккаунтом в веб-приложении.\n"
        "/cancel — выйти из пошагового заполнения профиля.\n\n"
        "А ещё можно просто написать вопрос — например, «где сейчас лучше работать?» — "
        "и я отвечу как ассистент.\n\n"
        "📷 Пришлите скрин карты повышенного спроса (Яндекс Про) — распознаю кэф "
        "и добавлю его в общую карту.\n\n"
        "Каждое утро пришлю план на день, предупрежу о всплеске спроса перед выездом "
        "и подведу итоги смены."
    )
